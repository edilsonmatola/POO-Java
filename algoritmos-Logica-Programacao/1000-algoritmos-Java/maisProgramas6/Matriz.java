//Este programa mostra matriz sem declarar um vector.
public class Matriz {
	public static void main (String args []){
		int linhas,colunas;
		for(linhas=1;linhas<7; linhas++){
			for(colunas=1; colunas<7; colunas++){
				System.out.print(linhas+" ");
				System.out.print(colunas+" ");
			}
			System.out.print(" ");
		}
		
	}
}
